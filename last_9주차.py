# list = [0, 1, 2, 3] -> 리스트끼리 합칠 수 있음, 일정 값까지 자를 수 있음
# tuple = ('a', 'b', 'c,) -> 요소값 추가/수정/삭제 불가
# dictionary = {'python':'파이썬', 'java':'자바', 'C':'씨-언어'} -> 키 값으로 설
#
# list.append(4) -> 리스트에 값 추가
# del list[0] -> 리스트 0번 값 제거
# list.remove(3) -> 리스트에 있는 3중 가장 첫 번째로 만나는 3을 삭제
# list.clear() -> 모든 요소 삭제
# list.index(1) -> 리스트 내에 인수값과 일치하는 첫 번째 요소의 인덱스 값, 존재하지 않을경우 -1 반환
# list.count(2) -> 리스트 내에 인수값과 일치하는 요소의 개수 반환
